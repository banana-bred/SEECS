! ================================================================================================================================ !
module seecs__system
! ================================================================================================================================ !

  use iso_fortran_env, only: stdout => output_unit, stderr => error_unit, stdin => input_unit

  implicit none (type, external)

  private

  public :: die
  public :: stdin
  public :: stderr
  public :: stdout

! ================================================================================================================================ !
contains
! ================================================================================================================================ !

  ! ------------------------------------------------------------------------------------------------------------------------------ !
  pure elemental subroutine die(message)
    !! Stop program execution with a message
    implicit none (type, external)
    character(*), intent(in), optional :: message
    if(.not.present(message)) error stop ; error stop message
  end subroutine die

! ================================================================================================================================ !
end module seecs__system
! ================================================================================================================================ !
